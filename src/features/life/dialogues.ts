export interface Line {
  id: string;
  text: string;
}

export function linesOf(cat: string): Line[] {
  const arr = POOL[cat] || [];
  return arr.map((text, i) => ({ id: `${cat}#${i}`, text }));
}

// プール全体の本数（デバッグ表示用）
export function poolSize(): number {
  return Object.values(POOL).reduce((n, a) => n + a.length, 0);
}
